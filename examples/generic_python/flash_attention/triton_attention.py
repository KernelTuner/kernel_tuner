def flash_attention_reference(q, k, v, causal=False, sm_scale=1.0):
    """
    Reference attention using batched matmul (like the pytest does).
    Args:
        q, k, v: [batch, heads, seq_len, head_dim]
        causal: whether to apply causal mask
        sm_scale: scaling factor for attention scores
    Returns:
        output: [batch, heads, seq_len, head_dim]
    """
    # Batched matrix multiply
    # scores: [batch, heads, seq_len, seq_len]
    scores = torch.matmul(q, k.transpose(-2, -1)) * sm_scale

    if causal:
        seq_len = q.shape[2]
        mask = torch.tril(torch.ones(seq_len, seq_len, device=q.device, dtype=torch.bool))
        scores = scores.masked_fill(~mask[None, None, :, :], float("-inf"))

    # Softmax along last dimension
    attention = torch.softmax(scores.float(), dim=-1)

    # Weighted sum with V
    output = torch.matmul(attention, v)
    return output



